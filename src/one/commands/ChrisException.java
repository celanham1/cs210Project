/**
 * The Exception ChrisException.
 */
public class ChrisException extends Exception {
	
	/**
	 * Instantiates a new Chris exception.
	 */
	public ChrisException(String message, Throwable cause) {
		super(message, cause);
		//cause.printStackTrace();
	}

	/**
	 * Instantiates a new Chris exception.
	 */
	public ChrisException() {
	}
	
	/**
	 * Instantiates a new Chris exception.
	 */
	public ChrisException(String message){
		super(message);
	}
	
	/**
	 * Instantiates a new Chris exception.
	 */
	public ChrisException(Throwable cause){
		super(cause);
		//cause.printStackTrace();
	}
	

	
	
}
