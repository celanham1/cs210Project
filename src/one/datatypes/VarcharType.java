package one.datatypes;

/**
 * The Varchar Data Type.
 */
public class VarcharType extends AbstractType{

	/**
	 * Instantiates a new varchar type.
	 *
	 * @param name the name
	 * @param type the type
	 */
	public VarcharType(String name, String type) {
		super(name, type);
		byteSize = 8;
	}
	
}

